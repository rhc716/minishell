#include "../minishell.h"

void ft_env(char **arg, char *envp[])
{
	int i;

	i = 0;
	while (envp[i])
	{
		write(1, envp[i], ft_strlen(envp[i]));
		write(1, "\n", 1);
		i++;
	}
}